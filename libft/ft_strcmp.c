#include "libft.h"

int	ft_strcmp(const char *str1, char *str2)
{
	unsigned int	i;

	i = 0;
	while (str1[i] && str2[i] && str1[i] == str2[i])
	{
		i++;
	}
	return (str2[i] - str1[i]);
}
